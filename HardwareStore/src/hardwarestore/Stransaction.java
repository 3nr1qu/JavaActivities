private void DisplayClerks()
{
    try{
                        Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/gadgetsdb","root","");
                        St = (Statement) Con.createStatement();
                        Rs = St.executeQuery("SELECT stransactions.STransactionID,stransactions.InventoryID,inventory.ProductName,stransactions.TypeOfTransaction, stransactions.Quantity,stransactions.UserID,users.LastName,stransactions.Date,stransactions.Time FROM stransactions INNER JOIN users ON stransactions.UserID = users.UserID INNER JOIN inventory ON inventory.InventoryID = stransactions.InventoryID;");
                        ClerksTransaction.setModel(DbUtils.resultSetToTableModel(Rs));
    }catch (Exception e){
        
    }
}
